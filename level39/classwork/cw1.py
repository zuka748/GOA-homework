## 1. დაწერეთ პროგრამა რომელსაც გადმოეცემა ნებისმიერი ლისტი სადაც ელემენტებად გადაცემულაი ინტეჯერები, თქვენი მიზანია გადაუყვეთ ამ ლისტს (while loop) და გამოიტანოთ ამ რიცხვების ჯამი.

def sum_with_while(numbers):
    i = 0
    total = 0
    while i < len(numbers):
        total += numbers[i]
        i += 1
    print("რიცხვების ჯამი არის:", total)