# Immutable (შეუცვლელი)
# შექმნის შემდეგ მნიშვნელობა აღარ იცვლება — ნებისმიერი ცვლილების მცდელობა ქმნის ახალ ობიექტს.

# თუ იმავე ცვლადში ახალი მნიშვნელობა ჩაწერე, სინამდვილეში ის ახალ ობიექტზე მიუთითებს, ხოლო ძველი უცვლელი რჩება.


