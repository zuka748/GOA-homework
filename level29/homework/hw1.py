#გამოიყენეთ Comprasion Operations და Logical Operations ერთად, შეეცადეთ კომენტარებით ახსნათ რატომ გამოაქვს ესეთი შედეგი და ასევე თუ რომელი სრულდება პირველი, რა თანმიმდევრობით. 
a = 10
b = 5
c = 20

result = a > b and c > a
# 1. შედარებები სრულდება პირველ რიგში:
#    a > b → 10 > 5 → True
#    c > a → 20 > 10 → True
# 2. შემდეგ სრულდება ლოგიკური ოპერატორი 'and':
#    True and True → True

print(result)  # გამოიტანს: True
